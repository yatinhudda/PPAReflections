package Serilization;

public class ScienceMarks {
    private final int phyMarks;
    private final int chemMarks;
    private final int mathMarks;
    private final double sciencePercentage;

    public ScienceMarks(int phyMarks, int chemMarks, int mathMarks, double sciencePercentage) {
        this.phyMarks = phyMarks;
        this.chemMarks = chemMarks;
        this.mathMarks = mathMarks;
        this.sciencePercentage = sciencePercentage;
    }
}
